N=int(input("Δώσε το πλήθος των ακεραίων:"))

i=1
a=0
b=0
c=0
while i<=N:
      print("Δώσε τον ακέραιο",i,":")
      a=int(input())
      i=i+1
      if a>0:
          c=c+a
      else:
          b=a+b
print("Το άθροισμα των θετικών ακεραίων είναι:",c)
print("Το άθροισμα των αρνητικών ακεραίων είναι:",b)
